const month = 3

switch (month) {
    case 1:
        console.log(`The month is jan`);
        break;
    case 2:
        console.log(`The month is feb`);
        break;
    case 3:
        console.log(`The month is march`);
        break;
    case 4:
        console.log(`The month is apr`);
        break;
        


    default:
        console.log("month does not exist!!!");
        
        break;
}